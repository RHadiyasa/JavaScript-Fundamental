// Synchronous
function syncOne() {
  console.log("Function One is done");
}

function syncTwo() {
  console.log("Function Two is done");
}

function syncThree() {
  console.log("Function Three is done");
}

syncTwo();
syncOne();
syncThree();

// Ketika kita memiliki kode Synchronous, maka kode akan di eksekusi dari atas ke bawah.