// Addition +
const x = 10;
const y = 20;
const result = x + y;

console.log("X = " + x);
console.log("Y = " + y);
console.log(x + " + " + y + " = " + result);

// Substraction - 
const substractionResult = y - x;
console.log(y + " - " + x + " = " + substractionResult);

// Multiplication *
const multiplicationResult = x * y;
console.log(x + " * " + y + " = " + multiplicationResult);

// Division /
const divisonResult = y / x;
console.log(y + " / " + x + " = " + divisonResult);

// Exponentiation **
const z = 2;
const exponentResult = x ** z;
console.log(x + " ** " + z + " = " + exponentResult);

// Modulus %
const mod = 7;
const modResult = y % mod;
console.log(y + " % " + mod + " = " + modResult);

